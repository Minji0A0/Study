package javaBasic10.com.javaprogram;

public class MathProgram {
    public static void main(String[] args) {
        System.out.println(Math.abs(-3.57));
        System.out.println(Math.max(5,100));
        System.out.println(Math.min(5,100));
        System.out.println(Math.random());
        System.out.println(Math.sqrt(9));
        System.out.println(Math.pow(2,5));
        System.out.println(Math.ceil(9.9));
        System.out.println(Math.floor(9.9));
        System.out.println(Math.round(9.9));
    }

}
